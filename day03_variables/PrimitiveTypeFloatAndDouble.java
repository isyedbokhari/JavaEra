package day03_variables;

public class PrimitiveTypeFloatAndDouble {

	public static void main(String[] args) {
		int num = (int) 9.99;
		// assigning a double value to int data type
		System.out.println(num);
// byte , short , int , long can only store whole number
		float price = 9.99f;
		System.out.println(price);
		double amount = 10.99;
		System.out.println(amount);
		long phoneNumber = 8378_738789324L;
		System.out.println(phoneNumber);
		
		// primitive data type = boolean  float , byte , char , long , double ,int , short
		// syntax
		// dataType variableName = value;
		
		
		
		
		
	}

}
