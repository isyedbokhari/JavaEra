package day03_variables;

public class PrimitiveDataType {

	public static void main(String[] args) {
		
// primitive data type = boolean , byte , int , char , long , float , short ,  double 
		// dataType variableName = value;
		
		boolean result = true;
		byte age = 60;
		short accountNumber = 12345;
		char digit = '9';
		// only single quote for char data type
		
		int distance = 300;
		long phoneNumber = 734673678832L;
		// always put "L" at the end of long data type
		
		float price = 743.1f;
		// always put "f" at the end of float data type
		
		double balance = 74637634;
		
		boolean isSundayToday = false;
		boolean isCoffeeReady= true;
		boolean isMondayToday = false;
		boolean isTimeForBreak = false;
		
		System.out.println(isSundayToday);
		System.out.println(isCoffeeReady);
		System.out.println(isMondayToday);
		System.out.println(isTimeForBreak);
	}

}
