package day06_operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		// = , += , -= , /=
		
		int a = 1;
		int b = 3;
		System.out.println(a);
		a += b; // a = a +b
		
		System.out.println(a);
		
		a += 7;
		System.out.println(a);
		
		
		
		
		
		
		
		
	}

}
